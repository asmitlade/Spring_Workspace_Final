package com.nt.bo;

public class EmployeeBO {
	private String ename;
	private String job;
	private double bsal;
	private double gsal;
	private double nsal;
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public double getBsal() {
		return bsal;
	}
	public void setBsal(double bsal) {
		this.bsal = bsal;
	}
	public double getGsal() {
		return gsal;
	}
	public void setGsal(double gsal) {
		this.gsal = gsal;
	}
	public double getNsal() {
		return nsal;
	}
	public void setNsal(double nsal) {
		this.nsal = nsal;
	}
}