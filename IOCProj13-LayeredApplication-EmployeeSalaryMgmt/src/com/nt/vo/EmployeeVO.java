package com.nt.vo;

public class EmployeeVO {
	private String ename;
	private String job;
	private String bsal;
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public String getBsal() {
		return bsal;
	}
	public void setBsal(String bsal) {
		this.bsal = bsal;
	}
}